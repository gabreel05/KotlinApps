class Calculator {
    fun plus(numA: Int, numB: Int): Int {
        return numA + numB
    }

    fun minus(numA: Int, numB: Int): Int {
        return numA - numB
    }

    fun times(numA: Int, numB: Int): Int {
        return numA * numB
    }

    fun div(numA: Int, numB: Int): Int {
        return numA / numB
    }
}
